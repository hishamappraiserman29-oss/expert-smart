# ── AV10: audit logging ───────────────────────────────────────────────────────

class TestVerifyAuditLogging:
    def test_AV10_verify_call_is_audited(self, client, tmp_path, monkeypatch):
        monkeypatch.setenv("AUDIT_ENABLED", "true")
        monkeypatch.setenv("AUDIT_DB_PATH", str(tmp_path / "audit.db"))
        monkeypatch.setenv("REPORTS_DB_PATH", str(tmp_path / "reports.db"))
        token = generate_token(_ADMIN_ID)
        client.get("/api/auth/verify",
                   headers={"Authorization": f"Bearer {token}"})
        import sqlite3
        conn = sqlite3.connect(str(tmp_path / "audit.db"))
        tables = [r[0] for r in conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table'"
        )]
        audit_tables = [t for t in tables
                        if any(k in t.lower() for k in ("audit", "log", "access"))]
        if audit_tables:
            count = conn.execute(
                f"SELECT COUNT(*) FROM {audit_tables[0]} WHERE endpoint LIKE '/api/auth%'"
            ).fetchone()[0]
            assert count >= 1
        conn.close()
