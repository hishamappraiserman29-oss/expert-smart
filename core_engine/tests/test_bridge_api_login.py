# ── AV01–AV03: token presence ─────────────────────────────────────────────────

class TestVerifyTokenPresence:
    def test_AV01_no_token_returns_401(self, client):
        r = client.get("/api/auth/verify")
        assert r.status_code == 401
        assert r.get_json()["status"] == "unauthorized"

    def test_AV02_invalid_token_returns_401(self, client):
        r = client.get("/api/auth/verify",
                       headers={"Authorization": "Bearer not.a.valid.token"})
        assert r.status_code == 401

    def test_AV03_valid_token_returns_200(self, client):
        token = generate_token(_ADMIN_ID)
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {token}"})
        assert r.status_code == 200
        body = r.get_json()
        assert body["status"] == "ok"
        assert body["user_id"] == _ADMIN_ID


# ── AV04–AV06: identity fields ────────────────────────────────────────────────

class TestVerifyIdentityFields:
    def test_AV04_admin_user_returns_is_admin_true(self, client):
        token = generate_token(_ADMIN_ID)
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {token}"})
        assert r.get_json()["is_admin"] is True

    def test_AV05_non_admin_user_returns_is_admin_false(self, client):
        token = generate_token(_REGULAR_ID)
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {token}"})
        body = r.get_json()
        assert body["status"] == "ok"
        assert body["user_id"] == _REGULAR_ID
        assert body["is_admin"] is False

    def test_AV06_user_id_in_response_matches_token_sub(self, client):
        token = generate_token("operator-007")
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {token}"})
        assert r.get_json()["user_id"] == "operator-007"


# ── AV07–AV08: token lifecycle ────────────────────────────────────────────────

class TestVerifyTokenLifecycle:
    def test_AV07_expired_token_returns_401(self, client):
        token = generate_token(_ADMIN_ID, ttl_seconds=1)
        time.sleep(2)
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {token}"})
        assert r.status_code == 401

    def test_AV08_tampered_token_returns_401(self, client):
        token = generate_token(_ADMIN_ID)
        tampered = token[:-4] + "XXXX"
        r = client.get("/api/auth/verify",
                       headers={"Authorization": f"Bearer {tampered}"})
        assert r.status_code == 401


